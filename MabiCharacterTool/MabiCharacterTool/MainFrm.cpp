int CMainFrame::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CFrameWnd::OnCreate(lpCreateStruct) == -1)
		return -1;

	if (!m_wndStatusBar.Create(this))
	{
		TRACE0("상태 표시줄을 만들지 못했습니다.\n");
		return -1;      // 만들지 못했습니다.
	}
	m_wndStatusBar.SetIndicators(indicators, sizeof(indicators)/sizeof(UINT));

	return 0;
}

BOOL CMainFrame::OnCreateClient(LPCREATESTRUCT /*lpcs*/,
	CCreateContext* pContext)
{
	

	if (!m_wndSplitter.CreateStatic(this, 1, 2))
	{
		return FALSE;
	}

	if (!m_wndSplitter.CreateView(0, 0,
		RUNTIME_CLASS(MainView), CSize(1280, 100), pContext))
	{
		return FALSE;
	}



	if (!m_wndSplitter.CreateView(0, 1,
		RUNTIME_CLASS(ControlView), CSize(1920 - 1280, 100), pContext))
	{
		return FALSE;
	}

	g_View = (MainView*)m_wndSplitter.GetPane(0, 0); // << 
	g_hWnd = m_wndSplitter.GetPane(0, 0)->m_hWnd;

	return TRUE;
}

BOOL CMainFrame::PreCreateWindow(CREATESTRUCT& cs)
{
	if( !CFrameWnd::PreCreateWindow(cs) )
		return FALSE;
	// TODO: CREATESTRUCT cs를 수정하여 여기에서

	cs.x = 1;
	cs.y = 1;
	cs.cx = 1920;
	cs.cy = 1080;

	cs.style &= ~FWS_ADDTOTITLE;
	LPCTSTR TitleName = _T("MabiCharacterTool");
	SetTitle(TitleName);
	//  Window 클래스 또는 스타일을 수정합니다.

	return TRUE;
}

